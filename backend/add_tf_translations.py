def find_translation(greek_text):
    for substring, ru in TRANSLATIONS.items():
        if substring in greek_text:
            return ru
    return None


def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    cur.execute("""
        SELECT id, content
        FROM exercises
        WHERE type = 'TRUE_FALSE'
          AND json_extract(content, '$.text_ru') IS NULL
    """)
    rows = cur.fetchall()

    print(f"Found {len(rows)} TRUE_FALSE exercises without text_ru\n")

    updated = 0
    no_match = 0

    for row_id, content_str in rows:
        content = json.loads(content_str)
        greek_text = content.get("text", "")
        translation = find_translation(greek_text)

        if translation:
            content["text_ru"] = translation
            new_content = json.dumps(content, ensure_ascii=False)
            cur.execute(
                "UPDATE exercises SET content = ? WHERE id = ?",
                (new_content, row_id),
            )
            print(f"✓ id={row_id}: {greek_text[:60]}...")
            updated += 1
        else:
            print(f"✗ NO MATCH id={row_id}: {greek_text[:60]}...")
            no_match += 1

    conn.commit()
    conn.close()

    print(f"\nDone. Updated: {updated}, No match: {no_match}, Total: {len(rows)}")


if __name__ == "__main__":
    main()
