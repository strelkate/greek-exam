def main():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    cur = conn.cursor()

    # Fetch all multiple_choice exercises that don't already have question_ru
    cur.execute("""
        SELECT id, content
        FROM exercises
        WHERE type = 'multiple_choice'
          AND json_extract(content, '$.question_ru') IS NULL
    """)
    rows = cur.fetchall()
    print(f"Found {len(rows)} multiple_choice exercises without question_ru")

    updated = 0
    skipped = 0

    for row in rows:
        exercise_id = row["id"]
        content = json.loads(row["content"])
        question = content.get("question", "")
        question_lower = question.lower()

        matched_ru = None
        for (substr1, substr2, ru) in TRANSLATIONS:
            s1_lower = substr1.lower()
            if s1_lower in question_lower:
                if substr2 is None or substr2.lower() in question_lower:
                    matched_ru = ru
                    break

        if matched_ru is None:
            print(f"  SKIP id={exercise_id}: no match for question: {question[:60]!r}")
            skipped += 1
            continue

        content["question_ru"] = matched_ru
        new_content = json.dumps(content, ensure_ascii=False)
        cur.execute(
            "UPDATE exercises SET content = ? WHERE id = ?",
            (new_content, exercise_id)
        )
        print(f"  UPDATED id={exercise_id}: {question[:60]!r}")
        updated += 1

    conn.commit()
    conn.close()
    print(f"\nDone. Updated: {updated}, Skipped: {skipped}")


if __name__ == "__main__":
    main()
